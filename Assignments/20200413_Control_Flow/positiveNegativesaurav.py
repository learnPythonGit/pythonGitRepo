# Write a program to check if the given number is +ve or -Ve or its 0. (using if else)

number = int(input("Please Enter a number"))

if number < 0:
    print("Entered Number is a negative Number")
elif number > 0:
    print("Entered Number is a positive Number")
else:
    print("Number entered is 0")


